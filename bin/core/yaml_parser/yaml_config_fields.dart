abstract class YamlConfigFields {
  /// Поле с данными сборки.
  static const String buildField = 'build';

  /// Поле с данными флейвора для сборки.
  static const String flavorField = 'flavor';

  /// Поле с путём до файла.
  static const String filePathField = 'file_path';

  /// Поле с флагами для сборки.
  static const String flagsField = 'flags';

  /// Поле с названием расширения.
  static const String extensionField = 'extension';
}
